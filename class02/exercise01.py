# Write a for loop that prints out from 0 to 10
# (including 0, excluding 10)
for n in range(10):
	print(n)

# Write a for loop that prints out the numbers 10 to 0
# (including 10, excluding 0)
for n in range(10, 0, -1):
	print(n)

# Write a while loop that will run forever
while True:
	print('running forever')

# Write a while loop that will never run
while False:
	print('you will never see me')
